<p>
With Envaya, your civil society organization can have its own website, for free, where you can share news about your projects with other organizations and people around the world.
</p>
<p>
<strong>Easy</strong>: To make a website on Envaya, you don't need any special computer training or equipment. In about five minutes, you can set up a basic home page yourself.
</p>
<p>
<strong>Free</strong>: Hiring someone to build and host a website for your organization can be expensive. Envaya is free.
</p>
<p>
<strong>Convenient</strong>: You can add new news and photos to your website by sending messages from your mobile phone. (Coming soon)
</p>
<p>
<strong>Visible</strong>: Envaya links your website into its network so that people can easily find you when they are looking for civil society organizations like yours.
</p>
<p>
<strong>Translated</strong>: Envaya is translated in Kiswahili and English, so you can use whichever language you prefer. When somebody visits your site who can't read your language, Envaya will translate your content into their language.
</p>
